import array as arr

intArray = arr.array('i', [1,2,3,4,5])
print(type(intArray))
print(intArray)

charArray = arr.array('u', ['R', 'o', 'h', 'i', 't'])
print(type(charArray))
print(charArray)

floatArray = arr.array('f', [1.1, 2.2, 3.3, 4.4])
print(type(floatArray))
print(floatArray)

doubleArray = arr.array('d', [1.0, 2.3, 4.5, 6.5])
print(type(doubleArray))
print(doubleArray)
